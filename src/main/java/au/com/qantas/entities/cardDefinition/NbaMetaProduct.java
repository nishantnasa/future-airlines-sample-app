public class NbaMetaProduct {

    public String productFamily;
    public String productVertical;
    public String productLabel;
    public String productBrand;
    public String productSKU;
    public String productOffering;


    public String getProductFamily() {
        return productFamily;
    }

    public void setProductFamily(String productFamily) {
        this.productFamily = productFamily;
    }

    public String getProductVertical() {
        return productVertical;
    }

    public void setProductVertical(String productVertical) {
        this.productVertical = productVertical;
    }

    public String getProductLabel() {
        return productLabel;
    }

    public void setProductLabel(String productLabel) {
        this.productLabel = productLabel;
    }

    public String getProductBrand() {
        return productBrand;
    }

    public void setProductBrand(String productBrand) {
        this.productBrand = productBrand;
    }

    public String getProductSKU() {
        return productSKU;
    }

    public void setProductSKU(String productSKU) {
        this.productSKU = productSKU;
    }

    public String getProductOffering() {
        return productOffering;
    }

    public void setProductOffering(String productOffering) {
        this.productOffering = productOffering;
    }
}
